package com.company;

public class GameChangeInfo {
    private int i;
    private int j;
    private int label;

    public GameChangeInfo(int i, int j, int label) {
        this.i = i;
        this.j = j;
        this.label = label;
    }

    public int getI() {
        return i;
    }

    public int getJ() {
        return j;
    }

    public int getLabel() {
        return label;
    }
}
